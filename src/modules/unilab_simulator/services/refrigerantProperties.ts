function interpolate(table: number[][], T: number, col: number): number {
  if (T <= table[0][0]) return table[0][col];
  if (T >= table[table.length - 1][0]) return table[table.length - 1][col];
  for (let i = 0; i < table.length - 1; i++) {
    const T0 = table[i][0], T1 = table[i + 1][0];
    if (T >= T0 && T <= T1) {
      const frac = (T - T0) / (T1 - T0);
      return table[i][col] + frac * (table[i + 1][col] - table[i][col]);
    }
  }
  return table[table.length - 1][col];
}

export function getRefrigerantProps(
  refrigerant: string,
  T_C: number,
  phase: FluidPhase = "liquid"
): FluidProps | null {
  const key = refrigerant.replace(/-/g, "").toUpperCase();
  const table = LIQUID_TABLES[key];
  if (!table) return null;
  const rho   = interpolate(table, T_C, 1);
  const mu    = interpolate(table, T_C, 2) * 1e-6;
  const cp    = interpolate(table, T_C, 3);
  const k     = interpolate(table, T_C, 4) * 1e-3;
  const Pr    = interpolate(table, T_C, 5);
  const hvapTable = H_VAP_TABLES[key];
  const h_vap = hvapTable ? interpolate(hvapTable, T_C, 1) : undefined;
  return { rho_kg_m3: rho, mu_Pa_s: mu, cp_J_kgK: cp, k_W_mK: k, Pr, h_vap_J_kg: h_vap };
}

export const AVAILABLE_REFRIGERANTS = Object.keys(LIQUID_TABLES);

export function isRefrigerantAvailable(refrigerant: string): boolean {
  return refrigerant.replace(/-/g, "").toUpperCase() in LIQUID_TABLES;
}
